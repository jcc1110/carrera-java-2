    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
     Operation op = new Operation();
     String card = jTextField1.getText();
     String pass = jTextField2.getText();
     
     if(op.validateUser(card, pass))
     {
         JOptionPane.showMessageDialog(jButton1, "Welcome");
     }
     else
     {
         JOptionPane.showMessageDialog(jButton1, "Unknown user");
     }
    
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       if (jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Card number");
        }
        else {
            if (jTextField2.getText().isEmpty()){
               JOptionPane.showMessageDialog(null, "Password");
            }
            else {
            Operation op = new Operation();
            User user = new User();
        
            user.setCard(jTextField1.getText());
            user.setPass(jTextField2.getText());    
                
            op.consultBalance(user);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

